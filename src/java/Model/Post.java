package Model;

import java.util.Date;

/**
 *
 * @author nhat anh
 */
public class Post {
    private int id;
   private String title,topic,content,author;
   private Date date;
   public Post(){
       
   }

    public Post(int id, String title, String topic, String content, String author, Date date) {
        this.id = id;
        this.title = title;
        this.topic = topic;
        this.content = content;
        this.author = author;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
   
}
